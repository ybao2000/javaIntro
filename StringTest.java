public class StringTest {
  public static void main(String[] args) {
    // String -- non-primitive type
    String fruit = "apple";
    // print out "I like to eat ?"
    // concatenate string
    String text = "I like to eat " + fruit; 
    System.out.println(text);  

    // print out I am 7 year's old
    int age = 7;
    String text2 = "I am " + age + " year's old";
    System.out.println(text2);     
    
    double height = 5.9;
    System.out.println("I am " + height + " feet long");
  }

}
